;;; Teljes grafikus képernyő letölrése
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
CLS_GR_SCREEN:
    LD HL, (DCB_HM_2)
    LD DE, (DCB_HM_2)
    INC DE
    LD (HL), 0
    LD BC, 7999
    LDIR
    RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; Teljes szöveges mód bekapcsolása
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

SET_FULLTEXT_MODE:
;    LD H, 0
;    LD L, 200
;    LD (0x4008), HL
;    CALL ROM_SET_GLDL_HL
    LD A, 200
    CALL ROM_SET_DL_A
    LD H, 0
    CALL ROM_SET_GL_H
    RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; 192 soros grafikusmód bekapcsolása. A felső sor szöveges marad
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
SET_GRAPHMODE_192:
;    LD H, 192
;    LD L, 200
;    LD (0x4008), HL
    LD A, 200
;    CALL ROM_SET_GLDL_HL
;    RET
    CALL ROM_SET_DL_A
    LD H, 192
    CALL ROM_SET_GL_H
    RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; 176 soros grafikusmód bekapcsolása. A felső sor szöveges marad
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
SET_GRAPHMODE_176:
    LD H, 176
    CALL ROM_SET_GL_H
    RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; Alulról beúszó grafikus mód
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
SET_GRAPHMODE_192_UP:
    LD B, 193
GRAPHMODE_UP_LOOP:
    LD A, 193
    SUB B
    LD H, A
    ;LD L, 200
    ;LD (0x4008), HL
    PUSH BC
    PUSH AF
    CALL ROM_SET_GL_H
    POP AF
    NEG
    CALL BEEP_A
    POP BC
    DJNZ GRAPHMODE_UP_LOOP
    RET
