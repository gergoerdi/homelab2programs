GET_RANDOM_A:
    LD HL, (RANDOM_ADDR)
    INC HL
    LD A, H
    AND %00000111
    LD H, A
    LD A, (HL)
    LD (RANDOM_ADDR), HL
    RET

RANDOM_ADDR: DW 0
